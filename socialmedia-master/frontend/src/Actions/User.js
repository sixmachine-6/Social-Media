import axios from "axios";

export const loginUser = (email, password) => async (dispatch) => {
  try {
    dispatch({
      type: "LoginRequest",
    });

    // const { data } = await axios({
    //   url: "http:127.0.0.1:5000/api/v1/users/login",
    //   method: "POST",
    //   data: { email, password },
    // });
    const { data } = await axios({
      method: "POST",
      url: "http://127.0.0.1:5000/api/v1/users/login",
      data: {
        email,
        password,
      },
    });
    dispatch({
      type: "LoginSuccess",
      payload: data.user,
    });
  } catch (error) {
    dispatch({
      type: "LoginFailure",
      payload: error.response.data.message,
    });
  }
};

